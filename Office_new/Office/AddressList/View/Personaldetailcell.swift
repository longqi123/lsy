import UIKit

class Personaldetailcell: UITableViewCell {


    
    @IBOutlet weak var photoLab: UILabel!
    
    @IBOutlet weak var nameLab: UILabel!
    
    @IBOutlet weak var rightBtn: UIButton!
    
    override func awakeFromNib() {
        super.awakeFromNib()
