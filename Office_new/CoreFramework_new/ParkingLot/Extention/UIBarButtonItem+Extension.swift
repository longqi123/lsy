import UIKit

extension UIBarButtonItem {
	
	/// backBarButtonItem
	public static var back: UIBarButtonItem {
		return UIBarButtonItem(title: "", style: .plain, target: nil, action: nil)
	}
	
	/// leftBarButtonItem
	///
	/// - Parameters:
	///   - target:
	///   - action:
	public convenience init(target: Any?, action: Selector?) {
		self.init(image: #imageLiteral(resourceName: "icon_back_left"), style: .plain, target: target, action: action)
	}
	
}
